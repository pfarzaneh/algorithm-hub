package pfarzaneh.training.algorithms;

public final class ImmuDTO {

    private final String name;
    private final int salary;

    public ImmuDTO(String name, int salary) {
        this.name = name;
        this.salary = salary;
    }

    public String getName() {
        return name;
    }

    public int getSalary() {
        return salary;
    }

}
